function makeOCG_DATA () 
{
	let rrr = []
	for (var i = 3; i <= 38; i++) 
	{
		const trackName = 'Cam' + (i < 10 ? ('0'+ i) : i )

		var json = {
			track: trackName,
			en: trackName,
			images: [trackName],
		}

		let idx = TOCG_DATA_L.findIndex(ele => ele && ele.track == trackName);
		if (idx >= 0)
		{
			var findObj = TOCG_DATA_L[idx]
			json.en = findObj.en
			json.vi = findObj.vi || ''
			json.voca = findObj.voca || ''
			json.note = findObj.note || ''

		}

		rrr.push(json)
	}

	TOCG_DATA_L = rrr;
}


